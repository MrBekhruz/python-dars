import math

pi = 3.14
a = 1
b = 2
c = 5
print(round(pi)) # umumiy yaxlitlash
print(math.ceil(pi)) # uzidan katta qiymatgacha yaxlitlash
print(math.floor(pi)) # uzidan kichkina qiymatgacha yaxlitlash
print(abs(pi)) # agar uzgaruvchi manfiy qiymatda bulsa uni musbat qiymatga uzgartiradi
print(pow(pi,2)) # biza hozi uzgaruvchini kvadratga oshirdik chunmasang calculyatorni och
print(max(pi,a,b,c)) # bir nechta uzgaruvchilarni eng katta qiymatlari uchun
print(min(pi,a,b,c)) # bu essa minimum qiymatni olib beradi
